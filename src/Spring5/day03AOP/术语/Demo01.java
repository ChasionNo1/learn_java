package Spring5.day03AOP.术语;

public class Demo01 {
    /*
    AOP术语
    class User{
        add()
        update()
        select()
        delete()
    }

    1、连接点：
        类里面哪些方法可以被增强，这些方法成为连接点
    2、切入点
        实际被真正增强的方法，称为切入点
    3、通知（增强）
        （1）实际增强的逻辑部分称为通知（增强）
        （2）通知有多种类型：
            前置通知  增强方法之前执行
            后置通知  增强方法之后执行
            环绕通知  前后都执行
            异常通知  方法出现异常执行
            最终通知  finally

     4、切面
        把通知应用到切入点过程

    * */
}
